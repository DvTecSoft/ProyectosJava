package clases;

public class Cuadrado extends Poligono 
{
	private int lado;

	/**
	 * 
	 */
	public Cuadrado() 
	{
	}

	/**
	 * @param lado
	 */
	public Cuadrado(int lado) 
	{
		this.lado = lado;
	}

	/**
	 * @return the lado
	 */
	public int getLado() 
	{
		return lado;
	}

	/**
	 * @param lado the lado to set
	 */
	public void setLado(int lado) 
	{
		this.lado = lado;
	}
	
	
	
	
	
}
